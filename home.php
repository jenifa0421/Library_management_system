<p>Public libraries are often seen as “opportunity institutions,” opening doors to, and for, the disenfranchised. 6 People turn to libraries to access the internet, take a GED class, get help with a resumé or job search, and seek referrals to other community resources. A recent report by the Center for an Urban Future highlighted the benefits to immigrants, seniors, individuals searching for work, public school students and aspiring entrepreneurs: “No other institution, public or private, does a better job of reaching people who have been left behind in today’s economy, have failed to reach their potential in the city’s public school system or who simply need help navigating an increasingly complex world.”</p>
<p>
We need to attend more closely to such “social infrastructures,” the “facilities and conditions that allow connection between people,” says sociologist Eric Klinenberg. In a recent interview, he argued that urban resilience can be measured not only by the condition of transit systems and basic utilities and communication networks, but also by the condition of parks, libraries and community organizations: “open, accessible, and welcoming public places where residents can congregate and provide social support during times of need but also every day.” 11 In his book Heat Wave, Klinenberg noted that a vital public culture in Chicago neighborhoods drew people out of sweltering apartments during the 1995 heat wave, and into cooler public spaces, thus saving livfinity with cultural institutions. 


